package android.example.com.miniproyecto1_equipo4;

/**
 *  Clase que representa una compra de un articulo
 *
 *  @autor Alexander Garcia, Marco Lozano, Jorge Pinto
 */
class ArticuloComprar {

    String nombre;
    String precio;
    int foto;

    ArticuloComprar(String nombre, String precio, int foto) {
        this.nombre = nombre;
        this.precio = precio;
        this.foto = foto;
    }
}